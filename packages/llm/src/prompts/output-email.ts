/**
 * L2 模板层 · 邮件（email）
 *
 * 适用场景：
 *   - 客户沟通邮件 / 项目汇报 / 商务合作
 *   - 长度短（500-1000 字）但要有温度
 *
 * 与 talk_script 的差异：
 *   - 邮件可以被复读（不像话术听过就忘）
 *   - 邮件不需要"开场 hook"，开头就是称呼
 *   - 邮件结尾需要明确 CTA（具体可执行的下一步）
 *   - 邮件**不**要给"3 个变体"（邮件不能三个版本同时发）
 *
 * 复用 Vincent 哲学 7 条 + L0 脱 AI 味 8 条 + 数据真实性规则（在 system prompt 里）
 */

import type { OutputType } from './output-generate';

export const EMAIL_PROMPT_INJECTION = `
**当前输出类型：邮件（500-1000 字，客户沟通/项目汇报）**

**邮件特殊要求**：

1. **开头是称呼** —— 不用 hook，直接「您好，XX：」/「Hi XX」
2. **称呼后第一句必带温度** —— 不要直接进主题，先一句话说明这封邮件的目的（"跟进上次沟通" / "发您项目方案初稿" / "感谢昨天的会面"）
3. **3 个段落结构**：
   - 段 1：邮件目的 + 感谢/致意（50-100 字）
   - 段 2：核心内容 / 关键信息（300-500 字）
   - 段 3：明确 CTA + 后续时间线（100-200 字）
4. **明确 CTA** —— 邮件结尾**必**须有具体下一步（「如方便，本周三下午我们可以约 30 分钟同步」/「期待您周五前反馈」），**不**是"期待您的回复"
5. **专业但有温度** —— 用"您"不用"你"，但**不要**写成"感谢您的来信"这种客服腔
6. **不堆术语** —— 客户不懂的概念用一句话解释
7. **签名档** —— 结尾留 Vincent 的署名 + 联系方式占位符

**禁忌**：
- ❌ "首先...其次...最后..." 机械罗列
- ❌ "如有疑问，请随时联系我" 这种废话
- ❌ 模板腔（"您好，很高兴与您合作"）
- ❌ 没有明确 CTA 的结尾
`;

export function buildEmailUserPrompt(n: number): string {
  return `**邮件输出要求**：

### title
邮件的核心命题（10-20 字）—— 这**不**是邮件主题行，是 Vincent 自己记住的

### primary_version
完整邮件（500-1000 字），结构：
- 称呼（您好，XX：）
- 段 1：邮件目的 + 温度（50-100 字）
- 段 2：核心内容 / 关键信息（300-500 字）
- 段 3：明确 CTA + 后续时间线（100-200 字）
- 签名档（Vincent + 联系方式占位符）

### subject
3 个备选邮件主题行（10-25 字 / 个）—— 简洁、不夸张、不带感叹号

### key_quotes
1-3 个**邮件中可圈可点**的表达（既专业又有温度的句子）。邮件不需要金句太多，**不**像文章。

### usage_suggestion
建议发送时间 / 是否要 CC 谁 / 是否需要附件

### assetReferences
${n} 项，记录每张资产卡如何被引用（核心论点 / 数据支撑 / 案例）

请输出严格 JSON。`;
}