/**
 * L2 模板层 · 演讲稿（speech）
 *
 * 适用场景：
 *   - 客户分享会 / 行业大会 / 团队内部分享
 *   - 时长 15-25 分钟（约 3000-5000 字口语化稿）
 *
 * 与 article_full 的差异：
 *   - 口语化（"你"开头多于"我们"，短句更多）
 *   - 节奏感更强（开场 30 秒必抓人，每 3 分钟一个转折/小高潮）
 *   - 不写"标题"，写"开场 hook"
 *   - 不强调"标题 / 钩子段 / 章节标题"格式（演讲没有小标题）
 *
 * 复用 Vincent 哲学 7 条 + L0 脱 AI 味 8 条 + 数据真实性规则（在 system prompt 里）
 */

import type { OutputType } from './output-generate';

export const SPEECH_PROMPT_INJECTION = `
**当前输出类型：演讲稿（15-25 分钟，约 3000-5000 字口语化稿）**

**演讲稿特殊要求**：

1. **开场 hook 30 秒必抓人** —— 不要"今天我来分享..."，直接抛一个反常识判断 / 一个具体场景 / 一个让你夜不能寐的问题
2. **每 3 分钟一个转折** —— 不能线性平铺，必须有"反转 / 共鸣 / 顿悟"的节奏
3. **口语化** —— 用"你"多于"我们"，用"我"讲个人故事多于"研究表明"
4. **现场感** —— 加停顿标记（【停顿】）、语气词（笑 / 叹气）、互动（"你们有没有遇到过..."）
5. **金句收尾** —— 演讲结尾必须是一个能被记住的金句（不喊口号、不说教）
6. **不用 markdown 标题** —— 演讲没有小标题，用换行 + 节奏标记代替
7. **引用控制** —— 全篇不超过 1 处直接引用（演讲里引用听不清就废了）

**章节结构（口语化）**：

1. **开场 hook** (300-500 字)：一个反常识判断 / 具体场景 / 个人故事
2. **核心观点 1** (500-800 字)：主卡片的一句话洞察 + 一个具体案例
3. **核心观点 2** (500-800 字)：第二张卡的延展 / 反常识判断
4. **核心观点 3** (500-800 字)：第三张卡的边界 / 适用条件
5. **金句收尾** (200-300 字)：一个能被记住的金句 + 一个开放问题

**互动节奏（每个核心观点中插入 1-2 次）**：
- 提问互动："你们有没有遇到过..."
- 停顿标记：【停顿 2 秒】
- 笑声/共鸣："对吧？" / "是不是？"
- 反问："但你有没有想过..."
`;

export function buildSpeechUserPrompt(n: number): string {
  return `**演讲稿输出要求**：

### title
演讲的核心命题（10-20 字）—— 这不是要写在 PPT 上的标题，而是 Vincent 自己心里记住的核心命题

### primary_version
完整演讲稿（3000-5000 字口语化），结构：
- 开场 hook（300-500 字）：反常识 / 场景 / 故事
- 核心观点 1（500-800 字）：主卡洞察 + 案例
- 核心观点 2（500-800 字）：延展 / 反常识
- 核心观点 3（500-800 字）：边界 / 适用条件
- 金句收尾（200-300 字）：可被记住 + 开放问题

每段都要口语化，可以用停顿标记 / 互动提问。

### variants
3 个开场 hook 变体（300-500 字 / 个）：
- "反常识切入" —— 直接抛违反直觉的判断
- "场景切入" —— 描述一个具体场景让听众代入
- "个人故事切入" —— 讲你自己的经历

### key_quotes
3-5 个**适合演讲**的金句（短、有节奏、可被记住）。**不**是文章金句。

### usage_suggestion
建议时长 / PPT 建议 / 现场互动建议 / Q&A 准备问题

### assetReferences
${n} 项，记录每张卡被引用的位置（开场 / 观点 1 / 观点 2 / 收尾）
- coreInsightUsed: 这张卡的洞察是否被保留

请输出严格 JSON。`;
}