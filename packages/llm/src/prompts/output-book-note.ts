/**
 * L2 模板层 · 读书笔记（book_note）
 *
 * 适用场景：
 *   - 读了一本书/文章，结合自己的洞察做笔记
 *   - 适合 Vincent 的「判断力资产化」场景：把别人的书变成自己的卡片
 *
 * 与 article_full 的差异：
 *   - 主体是"作者的观点" + "我的延展/反思"
 *   - 必须有引文（书中的话），但不是堆砌
 *   - 长度更短（1000-1500 字，1-2 个核心观点）
 *   - 必须保留**原文出处**（书名/作者/章节）
 *
 * 复用 Vincent 哲学 7 条 + L0 脱 AI 味 8 条 + 数据真实性规则（在 system prompt 里）
 */

import type { OutputType } from './output-generate';

export const BOOK_NOTE_PROMPT_INJECTION = `
**当前输出类型：读书笔记（1000-1500 字，结合自己的洞察）**

**读书笔记特殊要求**：

1. **必须保留原文出处** —— 每个作者观点后标「——《书名》第 X 章，作者」
2. **"作者说" + "我说"清晰区分** —— 不要把作者的话和自己的话混在一起
3. **不要堆砌引文** —— 1 个核心观点配 1-2 处直接引文即可，不要"作者说...作者还说..."
4. **必须有 Vincent 自己的延展** —— 每个作者观点后必须有"但我想补一句..."或"我的判断是..."
5. **数据真实性** —— 引文必须来自书中原话（**不**要凭印象改写），如果不确定原文，标注「按 Vincent 记忆，待核实」
6. **结构**：
   - 书名 + 作者 + 一句话价值
   - 1-2 个核心观点（每个包含：作者原话 + 我的延展/反思/边界）
   - 适用边界（什么情况下这个观点不成立）
   - 行动建议（我会怎么用这个观点）

**禁忌**：
- ❌ 把书的话直接抄一遍，没有 Vincent 自己的判断
- ❌ 引用改写（不忠实原文）
- ❌ 「这本书改变了我」这种空洞开头
`;

export function buildBookNoteUserPrompt(n: number): string {
  return `**读书笔记输出要求**：

### title
笔记的核心命题（10-20 字）

### primary_version
完整读书笔记（1000-1500 字），结构：
- 书名 + 作者 + 一句话价值（不超过 50 字）
- 核心观点 1：作者原话（带出处）+ Vincent 延展/反思
- 核心观点 2：作者原话（带出处）+ Vincent 延展/反思
- 适用边界（什么情况下不成立）
- 行动建议（我会怎么用）

### variants
3 个开头变体（150-200 字 / 个）：
- "价值切入" —— 直接说这本书对我有什么用
- "问题切入" —— 抛一个我读这本书前没想清楚的问题
- "冲突切入" —— 抛一个这本书里和我原有认知相反的观点

### key_quotes
3-5 个**作者原话**（带出处标记「——书名，作者」），**不**是 Vincent 自己改写的

### usage_suggestion
建议使用方式（自己回顾 / 团队分享 / 公众号二创等）

### assetReferences
${n} 项，记录每张资产卡如何被引用（直接观点 / 延展锚点 / 反例）

请输出严格 JSON。`;
}